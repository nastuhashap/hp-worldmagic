<p>Времена, когда «Хогвардс» был наполнен детским смехом, прошли. 
Вольдеморт становится все опаснее, ведь уничтожить юного волшебника Гарри — его главная цель. 
Тайный Орден Феникса — это последняя надежда оградить мир от Черного Лорда. Но как защититься от 
его воздействия на сознание? Именно этому и стоит научиться Гарри. </p>


