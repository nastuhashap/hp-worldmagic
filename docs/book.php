<p>Навсегда покидая свой дом на Бирючинной улице,
Гарри Потер не мог и предположить какая опасность нависла над ним. 
Вольдеморт и Упивающиеся Смертью готовы на все, чтобы уничтожить волшебника. 
Защитное заклятье рассеялось, а это значит, что в последней битве с Темным Лордом придется рассчитывать 
лишь на свои силы. На чаше весов жизнь и смерть. </p>

