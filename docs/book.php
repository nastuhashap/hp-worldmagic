<p>Что-то страшное надвигается на «Хогвардс», 
ведь даже великий Дамблдор не может противостоять злу.
 Казалось бы, школа волшебства — это единственное место, где можно укрыться.
 Но как быть, если где-то рядом ходит тот, кто скрывает в себе темные силы? Перехитрить 
 судьбу и сохранить жизнь — главная цель для Гарри и его верных друзей. </p>


