<p>Новый учебный год сулит Гарри Поттеру массу невероятных приключений. 
Чего стоит только опаснейший Сириус Блэк, открывший настоящую охоту на юного мага. 
К тому же, предсказание профессора Трелони о скорой гибели Гарри не добавляет оптимизма. 
Как спасти себя и своих друзей от сторонника Вольдеморта? Это задача не из легких. </p>


