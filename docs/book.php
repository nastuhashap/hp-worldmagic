<p>Трудно чувствовать себя счастливым, когда живешь с людьми, которые тебя откровенно не любят. 
Так было до того момента, пока в доме на Бирючинной улице, где жил юный Гарри Поттер, 
не начали происходить чудеса. В свой 11й день рождения Гарри узнает, что он — не обычный мальчик, а настоящий волшебник,
 которому пришло время отправиться в школу колдовства «Хогвардс». </p>

Гарри Поттер и тайная комната
