<p>Казалось бы, лето — прекрасное время отдыха даже для юного волшебника.
 Но только не для Гарри Поттера, которому каникулы не принесли радости.
 Вернувшись в школу колдовства, Гарри предстоит разгадать тайну. Среди пустых коридоров «Хогвардс»
 слышен зловещий шепот, а неведомая сила превращает студентов в камень. Сможет ли Гарри разгадать, кто в этом замешан? </p>


