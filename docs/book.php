<p>Должно произойти настоящее чудо, чтобы Гарри Поттера допустили к участию в Тремудром Турнире,
 ведь ему еще нет положенных 17 лет. Но благодаря Кубку Огня, сделавшему свой выбор в пользу Гарри, 
 ему все же представится возможность поучаствовать в соревнованиях. Только юный волшебник и подумать не мог,
 что на кону будет не только победа, но и его собственная жизнь. </p>


Гарри Поттер и Орден Феникса
