<h1>Бузинная Палочка</h1>
<p>
История этой палочки начинается весьма неудачно.
 После того как палочка была использована Антиохом для убийства
 своего давнего врага он начал хвастаться своей непобедимостью 
 и в ту же ночь был убит ночью. Далее судьба палочки становится
 скрытой от всех. Через какое-то время слухи о ней всплывают 
 так же неожиданно, как гром среди ясного неба. Восточноевропейский 
 изготовитель волшебных палочек Григорович заявлял о том что,
 он использует свои знания полученные от бузиной палочки для создания
 своих собственных. Видимо, 
это было обусловлено желанием увеличить число покупателей на свои изделия. 
 </p>
