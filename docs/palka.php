Но данное заявление привлекало не только покупателей,
 но и воров. Одной темной ночью Геллерт Гриндевальд 
 украл бузинную палочку. Геллерт Гриндевальд был не обычным вором,
 он вместе с Альбусом Дамблдором, долгое время пытался собрать все
 дары смерти воедино. 
 После получения бузиной палочки Гриндельвальд стал собирать 
 сторонников для захвата министерства, он стал первым «кого
 нельзя называть”. И когда бузинная палочка натворила очень
 много зла, то с ней встретился Альбус Дамблдор. Несмотря на все 
 слухи о непобедимости бузинной палочки Дамблдор смог одолеть Геллерта
 и заполучить палочку, Гриндельвальд был отправлен в специальную тюрьму, 
 созданную для него. 

После смерти Дамблдора вся цепочка 
приключений бузиной палочки была раскрыта 
темному лорду Воландеморту и он открыв могилу 
Альбуса заполучил палочку судьбы, так тоже ее называли. 

Воландеморт не смог полностью использовать силу палочки,
 так как не победил ее последнего владельца — Дамблдора. 
 Гарри Поттер получил палочку и уничтожил ею Воландеморта, 
 после просто уничтожил саму палочку разломав ее на кусочки.