<h1>С чего все началось</h1>

<p>

В 1990 году в голове Джоан (автор Гарри Поттера) возник новый образ: 
мальчик-волшебник, который впоследствии стал известным во всем мире.
Этот персонаж через некоторое время сделал ее богатой и знаменитой. 
А все началось с переполненной электрички... 
</p>

