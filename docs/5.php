<p>
Рон Уизли — Руперт Гринт
</p>

Руперт Гринт узнал о кастинге на Гарри Поттера по телевизору, смотря по детскому каналу новости.
 Он был поклонником книг Роулинг, потому сразу же записался на пробы. До этого актерского опыта у него не было,
 разве что, школьные спектакли.
 Фильм принес Гринту мировую известность и с тех пор актер снимается. Сейчас у него уже больше 95 ролей в кино.

