<h1>Актеры</h1>

<p>
Гарри Поттер — Дэниэл Рэдклифф
</p>

У Дэниэла Рэдклиффа Гарри Поттер не был первой ролью. За год до премьеры он снялся в 
телевизионном фильме «Дэвид Копперфильд» и показался в эпизодической роли в «Портном из Панамы». 
Все это происходило в 1999 году и именно в этот же год Рэдклифф попробовался на роль Поттера. Роль принесла ему 
головокружительный успех и актер с тех пор только набирает обороты, снимаясь в кассовых фильмах наравне с голливудскими звездами.

