<p>
Гермиона Грейнджер — Эмма Уотсон
</p>
Еще в детстве Эмма Уотсон твердо решила заняться актерской карьерой, 
приняв участие в нескольких школьных постановках. Попробоваться на роль
 ей посоветовал учитель. После выхода первой части Поттерианы девочка, как и
 ее друзья Рэдклифф и Гринт, проснулась известной. Она продолжила актерскую карьеру и вполне успешно.

