<h1>Мантия-невидимка</h1>
<p>
Мантия Невидимка – это наверно единственный из даров смерти,
 который не был причиной множества смертей.
 Младший брат считается наиболее мудрым, так как не
 умер из-за своего дара. 

Игнатус отправился в Годрикову долину, г
де и был похоронен. После его мантия была обнаружена
 Дамблдором у Джеймса Поттера. В темные времена, когда Воландеморт 
 собирал сторонников и убивал всех, кто отказывался подчиниться ему,
 мантия невидимки была отдана Дамблдору на исследование. 
 </p>
