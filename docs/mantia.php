После своего исследования ткани Дамблдор обнаружил, 
что это не обычная мантия. Ведь в мире волшебников были и 
обычные мантии невидимки, но они теряли невидимость спустя некоторое
 время и не были столь хороши в любых обстоятельствах. По словам Джеймса 
 Поттера, мантия невидимка была у них более нескольких столетий. Обладателем 
 мантии невидимки остался Гарри Поттер.